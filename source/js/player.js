$(function() {
    $ui["player"] = $("audio.player")[0];

    $ui["now-playing"] = $(".now-playing");
    $(".play", $ui["now-playing"]).on("click", app.Player.toggle);
});

app.Player = (() => {
    const toggle = () => {
        if ($ui["player"].paused) {
            $ui["player"].play();
        } else {
            $ui["player"].pause();
        }
    };

    return {
        toggle
    };
})();
